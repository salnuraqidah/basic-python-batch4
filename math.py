x = 10
y = 3

# tambah
z1 = x+y
print("Hasil Pertambahan {} + {} = {}".format(x,y,z1))

# kurang
z2 = x-y
print("Hasil Pengurangan {} - {} = {}".format(x,y,z2))

# kali
z3 = x*y
print("Hasil Perkalian {} * {} = {}".format(x,y,z3))

# bagi
z4 = x/y
print("Hasil Pembagian {} / {} = {}".format(x,y,z4))

# bagi
z4 = x//y
print("Hasil Pembagian {} / {} = {}".format(x,y,z4))

# modulo
z5 = x%y
print("Hasil Modulo {} % {} = {}".format(x,y,z5))

# pangkat
z6 = x**y
print("Hasil Pangkat {} ^ {} = {}".format(x,y,z6))